void PnlWznmSeqDetail::handleDpchAppDoButAppViewClick(
			DbsWznm* dbswznm
			, DpchEngWznm** dpcheng
		) {
	ubigint jrefNew = 0;
	string sref;

	if (statshr.ButAppViewAvail && statshr.ButAppViewActive) {
		if (xchg->getIxPreset(VecWznmVPreset::PREWZNMIXCRDACCAPP, jref)) {
			sref = "CrdWznmApp";
			xchg->triggerIxRefSrefIntvalToRefCall(dbswznm, VecWznmVCall::CALLWZNMCRDOPEN, jref, 0, 0, sref, recSeq.appRefWznmMApp, jrefNew);
		};

		if (jrefNew == 0) *dpcheng = new DpchEngWznmConfirm(false, 0, "");
		else *dpcheng = new DpchEngWznmConfirm(true, jrefNew, sref);
	};
};

