/******************************************************************************
 class QryWznmRelList::VecVOrd
 ******************************************************************************/

uint QryWznmRelList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "tbl") return TBL;
	if (s == "srl") return SRL;
	if (s == "sup") return SUP;
	if (s == "ver") return VER;
	if (s == "typ") return TYP;
	if (s == "tos") return TOS;
	if (s == "tot") return TOT;
	if (s == "frs") return FRS;
	if (s == "frt") return FRT;

	return(0);
};

string QryWznmRelList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == TBL) return("tbl");
	if (ix == SRL) return("srl");
	if (ix == SUP) return("sup");
	if (ix == VER) return("ver");
	if (ix == TYP) return("typ");
	if (ix == TOS) return("tos");
	if (ix == TOT) return("tot");
	if (ix == FRS) return("frs");
	if (ix == FRT) return("frt");

	return("");
};

void QryWznmRelList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i = 1; i <= 9; i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};

