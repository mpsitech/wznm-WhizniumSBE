set<uint> PnlWznmConRec::StatShr::comm(
			const StatShr* comp
		) {
	set<uint> items;

	if (ixWznmVExpstate == comp->ixWznmVExpstate) insert(items, IXWZNMVEXPSTATE);
	if (jrefDetail == comp->jrefDetail) insert(items, JREFDETAIL);
	if (jrefAPar == comp->jrefAPar) insert(items, JREFAPAR);
	if (jrefSup1NControl == comp->jrefSup1NControl) insert(items, JREFSUP1NCONTROL);
	if (jrefFedRef1NRtblock == comp->jrefFedRef1NRtblock) insert(items, JREFFEDREF1NRTBLOCK);
	if (pnlfedref1nrtblockAvail == comp->pnlfedref1nrtblockAvail) insert(items, PNLFEDREF1NRTBLOCKAVAIL);
	if (ButRegularizeActive == comp->ButRegularizeActive) insert(items, BUTREGULARIZEACTIVE);

	return(items);
};

set<uint> PnlWznmConRec::StatShr::diff(
			const StatShr* comp
		) {
	set<uint> commitems;
	set<uint> diffitems;

	commitems = comm(comp);

	diffitems = {IXWZNMVEXPSTATE, JREFDETAIL, JREFAPAR, JREFSUP1NCONTROL, JREFFEDREF1NRTBLOCK, PNLFEDREF1NRTBLOCKAVAIL, BUTREGULARIZEACTIVE};
	for (auto it = commitems.begin(); it != commitems.end(); it++) diffitems.erase(*it);

	return(diffitems);
};

